CREATE TABLE CUSTOMER_CONSENT (
  ID NUMBER PRIMARY KEY,
  CUSTOMER_NAME VARCHAR2(500),
  IDCARD_NUMBER VARCHAR2(255),
  PHONE_NUMBER VARCHAR2(255),
  EMAIL VARCHAR2(255),
  STATUS_NOTI VARCHAR2(255),
  SOURCE_DATA VARCHAR2(255),
  SOURCE_STATUS_NOTI VARCHAR2(255),
  NOTI_CHANNEL VARCHAR2(255),
  REF_ID VARCHAR2(500),
  CIF_NUMBER VARCHAR2(500),
  NOTI_CONTENT CLOB,
  CREATED_DATE TIMESTAMP,
  CREATED_BY VARCHAR2(255),
  MODIFY_DATE TIMESTAMP,
  MODIFY_BY VARCHAR2(255),
  DOB TIMESTAMP,
  CHANNEL VARCHAR2(255),
  ISSUED_DATE TIMESTAMP,
  ISSUED_PLACE VARCHAR2(255),
  CUS_COMMENT VARCHAR2(255),
  REFERENCE VARCHAR2(255),
  EVIDENCE VARCHAR2(255),
);

CREATE SEQUENCE CUSTOMER_CONSENT_SEQ  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  GLOBAL ;